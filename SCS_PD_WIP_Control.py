    st.header(f"{dept_to} Receive")
    
    # ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö OS Receive ‡πÅ‡∏ú‡∏ô‡∏Å‡∏ñ‡∏±‡∏î‡πÑ‡∏õ‡∏ï‡πâ‡∏≠‡∏á‡πÄ‡∏õ‡πá‡∏ô "OS Transfer"
